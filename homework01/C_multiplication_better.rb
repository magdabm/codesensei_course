puts "Multiplication table"

a = 1

puts "      " "%-5d" % (a) + "%-5d" % (a+1) + "%-5d" % (a+2) + "%-5d" % (a+3) + "%-5d" % (a+4) +
              "%-5d" % (a+5) + "%-5d" % (a+6) + "%-5d" % (a+7) + "%-5d" % (a+8) + "%-5d" % (a+9)

puts "      ---- ---- ---- ---- ---- ---- ---- ---- ---- ---- "

i = 1

while i <= 10
  puts  "%-4d" % i + "| " + "%-5d" % (a*i) + "%-5d" % ((a+1)*i) + "%-5d" % ((a+2)*i) +
        "%-5d" % ((a+3)*i) + "%-5d" % ((a+4)*i) + "%-5d" % ((a+5)*i) + "%-5d" % ((a+6)*i) +
        "%-5d" % ((a+7)*i) + "%-5d" % ((a+8)*i) + "%-5d" % ((a+9)*i)
  i += 1
  a = 1
end
